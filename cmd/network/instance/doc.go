// Package instance provides commands for managing network instances.
//
// The commands in this package allow users to connect, disconnect, and list instances connected to a network.
//
// Examples:
//
//  # List all instances connected to a network
//  virak-cli network instance list --network-id <network-id>
//
//  # Connect an instance to a network
//  virak-cli network instance connect --network-id <network-id> --instance-id <instance-id>
//
//  # Disconnect an instance from a network
//  virak-cli network instance disconnect --network-id <network-id> --instance-id <instance-id>
