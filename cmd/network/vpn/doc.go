// Package vpn provides commands for managing VPN for a network.
//
// The commands in this package allow users to enable, disable, show, and update VPN for a network.
//
// Examples:
//
//  # Show VPN details for a network
//  virak-cli network vpn show --network-id <network-id>
//
//  # Enable VPN for a network
//  virak-cli network vpn enable --network-id <network-id>
//
//  # Disable VPN for a network
//  virak-cli network vpn disable --network-id <network-id>
package vpn
