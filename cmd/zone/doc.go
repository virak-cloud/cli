// Package zone provides commands for managing zones.
//
// The commands in this package allow users to list zones, and view networks, resources, and services within a zone.
//
// Examples:
//
//  # List all zones
//  virak-cli zone list
//
//  # List all networks in a zone
//  virak-cli zone networks --zone-id <zone-id>
//
//  # List all resources in a zone
//  virak-cli zone resources --zone-id <zone-id>
//
//  # List all services in a zone
//  virak-cli zone services --zone-id <zone-id>
package zone
